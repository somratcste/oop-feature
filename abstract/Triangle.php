<?php

require_once __DIR__. '/Shape.php';

class Triangle extends Shape
{
    private $color = null;
    private $name;

    public function getColor()
    {
        return $this->color;
    }

    public function setColor($color)
    {
        $this->color = $color;
    }

    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    public function getName()
    {
        return $this->name;
    }
}